# Datentypen und Werte

# Wahrheitswert (Boolean)
print("Wahrheitswert (Boolean)")
is_happy = False  # Definiere einen Wahrheitswert is_happy als False
is_happy = True   # Aktualisiere den Wahrheitswert auf True
print("Wert von is_happy:", is_happy)  # Gib den Wert von is_happy aus
print("Typ von is_happy:", type(is_happy))  # Gib den Typ von is_happy aus

# Ganzzahl (Integer)
print("Ganzzahl (Integer)")
x = 10  # Definiere eine Ganzzahl x mit dem Wert 10
print("Wert von x:", x)  # Gib den Wert von x aus
print("Typ von x:", type(x))  # Gib den Typ von x aus

# Fließkommazahl (Float)
print("Fließkommazahl (Float)")
pi = 3.14159  # Definiere eine Fließkommazahl pi mit dem Wert 3.14159
print("Wert von pi:", pi)  # Gib den Wert von pi aus
print("Typ von pi:", type(pi))  # Gib den Typ von pi aus

# Zeichenkette (String)
print("String")
name = "John"  # Definiere eine Zeichenkette name mit dem Wert "John"
print("Wert von name:", name)  # Gib den Wert von name aus
print("Typ von name:", type(name))  # Gib den Typ von name aus

# NoneType
print("NoneType")
nichts = None  # Definiere eine Variable nichts mit dem Wert None (kein Wert)
print("Wert von nichts:", nichts)  # Gib den Wert von nichts aus
print("Typ von nichts:", type(nichts))  # Gib den Typ von nichts aus
